package encapsulamento.objetcs;

public class GettersSetters {
    private String name;
    private double price;

    //obter o atributo
    public String getName() {
        return name;
    }

    //alterar o atributo
    public void setName(String name) {
        this.name = name;
    }
    public double getPrice() {
        return price;
    }
    public void setPrice(double price) {
        this.price = price;
    }
}
